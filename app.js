function showingModal (){
  let displaySomeSpec = document.querySelector(".result-div");
  displaySomeSpec.style.display = "none";
createDiv.style.display = "block";
}

function hideModal (){
  let displaySomeSpec = document.querySelector(".result-div");
  displaySomeSpec.style.display = "unset";
  createDiv.style.display = "none";
}